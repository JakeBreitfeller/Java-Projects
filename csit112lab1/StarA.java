package csit112lab1;

public class StarA {
	
	public static void main(String[] args) {
			
	for (int rows = 10; rows >= 1; rows--) {
        for (int columns = 1; columns <= rows; columns++) {
            System.out.print("*");
        }

        System.out.println();
		
		}
	
	}
}
